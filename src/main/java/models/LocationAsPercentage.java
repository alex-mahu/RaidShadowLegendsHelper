package models;

public final class LocationAsPercentage {
    public double xPercentage;
    public double yPercentage;

    public LocationAsPercentage() {
    }

    public LocationAsPercentage(double xPercentage, double yPercentage) {
        this.xPercentage = xPercentage;
        this.yPercentage = yPercentage;
    }
}
